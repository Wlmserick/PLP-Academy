void main() {
  //Numbers are divided into two categories, Integers and Doubles
  // Integers - used to store whole numbers.
  int num1 = 32;

  // Doubles - used to store decimal numbers.
  double weight = 46.04;

  // Strings - used to store text.
  String name = "Erick";

  // Lists - used to store collections of items.
  List<int> numbers = [32, 44, 56];

  // Maps - used to store key-value pairs.
  Map<String, int> ages = {'Kenya': 65, 'Australia': 40};

  //Demonstrate their usage.
  print("Integer: $num1");
  print("Double: $weight");
  print("String: $name");
  print("List: $numbers");
  print("Map: $ages");
}
